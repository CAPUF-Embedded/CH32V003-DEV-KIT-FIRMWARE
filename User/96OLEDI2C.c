void OLEDI2CCMD(uint8_t deviceAddr, uint8_t command)
{
    while( I2C_GetFlagStatus( I2C1, I2C_FLAG_BUSY ) != RESET );
    I2C_GenerateSTART( I2C1, ENABLE );

    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_MODE_SELECT ) );
    I2C_Send7bitAddress( I2C1, (deviceAddr << 1), I2C_Direction_Transmitter );

    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED ) );

    I2C_SendData( I2C1, (u8)(0x00));
    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED ) );


    I2C_SendData( I2C1, (u8)(command) );
    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED ) );

    I2C_GenerateSTOP( I2C1, ENABLE );
}

void OLEDI2CInit(void)
{
    uint8_t loop = 0;

    uint8_t command[30] = {
              0x20 //Set Memory Addressing Mode
            , 0x10 //00,Horizontal Addressing Mode;01,Vertical Addressing Mode;10,Page Addressing Mode (RESET);11,Invalid
            , 0xB0 //Set Page Start Address for Page Addressing Mode,0-7
            , 0xC8 //Set COM Output Scan Direction
            , 0x00 //---set low column address
            , 0x10 //---set high column address
            , 0x40 //--set start line address
            , 0x81 //--set contrast control register
            , 0xFF
            , 0xA1 //--set segment re-map 0 to 127
            , 0xA6 //--set normal display
            , 0xA8 //--set multiplex ratio(1 to 64)
            , 0x3F //
            , 0xA4 //0xa4,Output follows RAM content;0xa5,Output ignores RAM content
            , 0xD3 //-set display offset
            , 0x00 //-not offset
            , 0xD5 //--set display clock divide ratio/oscillator frequency
            , 0xF0 //--set divide ratio
            , 0xD9 //--set pre-charge period
            , 0x22 //
            , 0xDA //--set com pins hardware configuration
            , 0x12
            , 0xDB //--set vcomh
            , 0x20 //0x20,0.77xVcc
            , 0x8D //--set DC-DC enable
            , 0x14 //
            , 0xAF //--turn on SSD1306 panel
    };

    /* Init LCD */
    for(loop = 0; loop < 28; loop++)
    {
        OLEDI2CCMD(OLEDI2C_ADDR, command[loop]);
    }

}

void OLEDI2CSetPage(uint8_t pageNumber)
{
    OLEDI2CCMD(OLEDI2C_ADDR, (0xB0 + pageNumber));
}

void OLEDI2CSetCol(uint8_t col)
{

    OLEDI2CCMD(OLEDI2C_ADDR, (col & 0x0F)); // Low Column byte
    OLEDI2CCMD(OLEDI2C_ADDR, (0x10 + (col>>4))); // High Column byte

}

void OLEDI2CPutChar(uint8_t line, uint8_t startingColumn, char characterToPrint, uint8_t dataLength)
{
    //uint8_t loop = 0;

    OLEDI2CSetPage(line);
    OLEDI2CSetCol(startingColumn);

    uint8_t columnNumber = 0;
    uint8_t tmp = 0;

        while( I2C_GetFlagStatus( I2C1, I2C_FLAG_BUSY ) != RESET );
        I2C_GenerateSTART( I2C1, ENABLE );

        while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_MODE_SELECT ) );
        I2C_Send7bitAddress( I2C1, (OLEDI2C_ADDR << 1), I2C_Direction_Transmitter );

        while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED ) );

        I2C_SendData( I2C1, (u8)(0x40));
        while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED ) );


        for(columnNumber = 0; columnNumber < dataLength; columnNumber++)
        {
            tmp = AARR_GlcdFont_U8[characterToPrint-32][columnNumber];
            I2C_SendData( I2C1, (u8)tmp );
            while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED ) );
        }

        I2C_GenerateSTOP( I2C1, ENABLE );

}

void OLEDI2CPutStr(uint8_t line, uint8_t startingColumn, char* stringToPrint, uint8_t stringLength)
{
    while(stringLength)
    {
        OLEDI2CPutChar(line, startingColumn, *stringToPrint, 6);
        startingColumn +=6;
        stringToPrint++;
        stringLength--;
    }
}

void OLEDI2CFillScreen(uint8_t data)
{
    uint8_t pageNumber = 0;
    uint8_t tmp = 0;
    uint8_t buffer[128] = {0};

    for(tmp = 0; tmp < 128; tmp++) buffer[tmp] = data;

    for (pageNumber = 0; pageNumber < 8; pageNumber++)
    {
        OLEDI2CCMD(OLEDI2C_ADDR, 0xB0 + pageNumber);
        OLEDI2CCMD(OLEDI2C_ADDR, 0x00);
        OLEDI2CCMD(OLEDI2C_ADDR, 0x10);

        /* Write 128 bytes data */
        OLEDI2CWriteMuiltiByte(OLEDI2C_ADDR, 0x40, buffer, 128);

    }
}

void OLEDI2CWriteMuiltiByte(uint8_t deviceAddr, uint8_t memoryAddr, uint8_t *buffer, uint16_t length)
{
    uint8_t columnNumber = 0;
    uint8_t tmp = 0;

    while( I2C_GetFlagStatus( I2C1, I2C_FLAG_BUSY ) != RESET );
    I2C_GenerateSTART( I2C1, ENABLE );

    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_MODE_SELECT ) );
    I2C_Send7bitAddress( I2C1, (deviceAddr << 1), I2C_Direction_Transmitter );

    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED ) );

    I2C_SendData( I2C1, (u8)(memoryAddr));
    while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED ) );


    for(columnNumber = 0; columnNumber < length; columnNumber++)
    {
        tmp = buffer[columnNumber];
        I2C_SendData( I2C1, (u8)(tmp) );
        while( !I2C_CheckEvent( I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED ) );
    }

    I2C_GenerateSTOP( I2C1, ENABLE );

}

void OLEDI2CPattern(void)
{
    OLEDI2CFillScreen(0xFF); // Fill Full Screen
    Delay_Ms(100);
    OLEDI2CFillScreen(0x0F); // Fill Full Screen with alternate 4 row pixel fill and next 4 row pixel blank
    Delay_Ms(250);
    OLEDI2CFillScreen(0xF0); // Fill Full Screen with alternate 4 row pixel blank and next 4 row pixel fill
    Delay_Ms(250);
    OLEDI2CFillScreen(0x00); // Blank Full Screen
}
